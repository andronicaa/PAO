package stringuri.stringbuilder;
import java.lang.*;
public class Exemplu2 {
//    The StringBuilder represents a mutable sequence of characters
    public static void main(String[] args) {
        StringBuilder sb = new StringBuilder();
        sb.append("Buna ziua");
        System.out.println(sb);
        sb.reverse();
        System.out.println(sb);
    }


}
